#!/usr/bin/env node
/**
 * Konvertuje sermon HTML soubory na strukturovaný JSON pro Messages tab.
 *
 * Vstup:  ../api/Messages/pl-html/{date}.html   (např. 47-0412.html)
 * Výstup: ../api/Messages/pl-texts/{date}.json  (jeden soubor per kázání)
 *
 * Použití:
 *   node scripts/convert-messages-html.mjs pl
 *
 * Struktura každého výstupního souboru (pl-texts/{date}.json):
 * {
 *   "title": "Wiara jest substancją",
 *   "location": "Oakland, Kalifornia, USA",
 *   "chunks": [
 *     { "pnum": 1, "text": "Otrzymaliśmy kilka..." },
 *     { "pnum": 1, "text": "Ufam, że..." },
 *     { "pnum": 2, "text": "Więc ja myślę..." },
 *     ...
 *   ]
 * }
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");

const lang = process.argv[2] || "pl";
const inputDir = path.join(ROOT, "api", "Messages", `${lang}-html`);
const outputDir = path.join(ROOT, "api", "Messages", `${lang}-texts`);

// Pomocné regexy
const HEADER_RE = /<div class="message-header">([\s\S]*?)<\/div>/;
const H1_RE = /<h1>([\s\S]*?)<\/h1>/;
const H2_RE = /<h2>([\s\S]*?)<\/h2>/;
const TEXTBLOCK_RE = /<div class="textBlock">([\s\S]*?)<\/div>\s*(?=<div class="textBlock"|$)/g;
const WMB_RE = /<div class="wmb">([\s\S]*?)<\/div>(?=\s*<div class="wmb"|\s*<\/div>|\s*$)/g;
const PNUM_RE = /<div class="pnum">(\d+)<\/div>/;

/**
 * Strip HTML tagy, decode entity, normalizuj whitespace.
 * Funguje bez DOM (cheerio by byla další dependence).
 */
function cleanText(html) {
  return html
    .replace(/<div class="pnum">\d+<\/div>/g, "") // odstraň pnum div
    .replace(/<[^>]+>/g, " ") // strip ostatní tagy
    .replace(/&nbsp;/g, " ")
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/\s+/g, " ")
    .trim();
}

/**
 * Parsuje jeden HTML soubor → { title, location, chunks: [{pnum, text}] }
 */
function parseHtml(html) {
  // Header
  const headerMatch = html.match(HEADER_RE);
  let title = "";
  let location = "";
  if (headerMatch) {
    const header = headerMatch[1];
    const h1 = header.match(H1_RE);
    const h2 = header.match(H2_RE);
    title = h1 ? cleanText(h1[1]) : "";
    location = h2 ? cleanText(h2[1]) : "";
  }

  // Robustnější parsing přes balanced div counting (regex selhává na nested divs)
  const chunks = [];
  let currentPnum = 0;
  const wmbRegex =
    /<div class="wmb">([\s\S]*?)<\/div>(?=\s*<div class="wmb">|\s*<\/div>\s*<div class="textBlock">|\s*<\/div>\s*$)/g;

  let match;
  while ((match = wmbRegex.exec(html)) !== null) {
    const wmbContent = match[1];
    const pnumMatch = wmbContent.match(PNUM_RE);
    if (pnumMatch) currentPnum = Number(pnumMatch[1]);
    const text = cleanText(wmbContent);
    if (text) chunks.push({ pnum: currentPnum, text });
  }

  return { title, location, chunks };
}

// === MAIN ===
if (!fs.existsSync(inputDir)) {
  console.error(`Input dir does not exist: ${inputDir}`);
  console.error(`Create it and put {date}.html files inside.`);
  console.error(`For testing: cp pl-working-files/47-0412.html ${inputDir}/`);
  process.exit(1);
}

const files = fs
  .readdirSync(inputDir)
  .filter((f) => f.endsWith(".html"))
  .sort();

console.log(`Processing ${files.length} HTML files from ${inputDir}`);

fs.mkdirSync(outputDir, { recursive: true });

let parsedChunks = 0;
let failed = 0;

for (const file of files) {
  const dateKey = path.basename(file, ".html");
  try {
    const html = fs.readFileSync(path.join(inputDir, file), "utf8");
    const parsed = parseHtml(html);
    const outFile = path.join(outputDir, `${dateKey}.json`);
    fs.writeFileSync(outFile, JSON.stringify(parsed, null, 2), "utf8");
    parsedChunks += parsed.chunks.length;
    console.log(
      `  ${dateKey}: ${parsed.chunks.length} chunks  "${parsed.title}"`,
    );
  } catch (err) {
    console.error(`  ${dateKey}: FAILED — ${err.message}`);
    failed++;
  }
}

// Vygeneruj index.ts který staticky importuje všechny per-soubor JSONy
// a re-exportuje je jako jeden objekt { [dateKey]: MessageTexts }.
// Tohle používá Next/webpack pro full-text search v MessagesBrowser.
const indexEntries = files
  .map((f) => path.basename(f, ".html"))
  .filter((dateKey) => fs.existsSync(path.join(outputDir, `${dateKey}.json`)));

const importLines = indexEntries
  .map((d, i) => `import s${i} from "./${d}.json";`)
  .join("\n");
const mapLines = indexEntries
  .map((d, i) => `  "${d}": s${i},`)
  .join("\n");

const indexSrc = `// AUTO-GENERATED by scripts/convert-messages-html.mjs — do not edit by hand.
export interface MessageTexts {
  title: string;
  location: string;
  chunks: { pnum: number; text: string }[];
}

${importLines}

const texts: Record<string, MessageTexts> = {
${mapLines}
};

export default texts;
`;
fs.writeFileSync(path.join(outputDir, "index.ts"), indexSrc, "utf8");

console.log(
  `\nDone. ${files.length - failed}/${files.length} files, ${parsedChunks} chunks total.`,
);
console.log(`Written to: ${outputDir}/`);
console.log(`Index: ${outputDir}/index.ts (${indexEntries.length} entries)`);
